  function Invoke-MgGraphRequestPaginated {
  <#
  .SYNOPSIS
  Queries the Graph API using a paginated request format to gather all data possible from the URL
  .EXAMPLE
  Invoke-MGGraphRequestPaginated -Uri
  #>
  [CmdletBinding()]
    param (
      [Parameter(Mandatory=$false, HelpMessage="HTTP Method")]
      [string]$Method="GET",
      [Parameter(Mandatory=$true, HelpMessage="Microsoft Graph API Url to query for data")]
      [string]$Uri,
      [Parameter(Mandatory=$false, HelpMessage="Custom Content Type")]
      [string]$ContentType,
      [Parameter(Mandatory=$false, HelpMessage="Microsoft Graph API Url to query for data")]
      [ValidateSet("HashTable","PSObject","HttpResponseMessage", "Json")]
      [string]$OutputType="PsObject"
      # [Parameter(ValueFromRemainingArguments=$true)]
      # [object[]]$Args
    )

    begin {

    }

    process {
      try {
        $Response = (Invoke-MgGraphRequest -uri $Uri -Method Get -OutputType PSObject)
        $Dataset = $Response.value
        $NextLink = $Response."@odata.nextLink"
        
        while ($null -ne $NextLink) {
            $Response_Sub = (Invoke-MGGraphRequest -Uri $NextLink_Sub -Method Get -OutputType PSObject)
            $NextLink_Sub = $Response_Sub."@odata.nextLink"
            $Dataset += $Response_Sub.value
        }
      }
      catch {
        Throw "Error: $($_.Exception.Message)"
        return $false
      }
    }

    end {
      Return $Dataset
    }
  }

