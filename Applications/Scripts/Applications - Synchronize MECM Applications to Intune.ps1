  function Connect-MsGraph {
    [CmdletBinding()]
    param (
      [Parameter(Mandatory=$false, HelpMessage="Azure AD Tenant ID")]
      [string]$TenantId,
      [Parameter(Mandatory=$false, HelpMessage="Azure AD App Registration Client ID")]
      [string]$ClientId,
      [Parameter(Mandatory=$false, HelpMessage="Azure AD App Registration Client Secret")]
      [string]$ClientSecret,
      [Parameter(Mandatory=$false, HelpMessage="Required permissions")]
      [string[]]$Scopes
    )

    begin {

    }

    process {
      try {
        # Install Required Providers
        if (-not (Get-PackageProvider -ListAvailable -Name "NuGet")) {
          Write-Verbose "Installing the NuGet package provider"
          Install-PackageProvider -Name NuGet -Confirm:$false
          Import-PackageProvider -Name NuGet -Force
        }

        # Install Required Modules
        if (-not (Get-Module -ListAvailable -Name "Microsoft.Graph.Authentication")) {
          Write-Verbose "Installing the Microsoft.Graph.Authentication module"
          Install-Module -Name "Microsoft.Graph.Authentication" -Force -Scope CurrentUser
        }
  
        # Connect Using Interactive Authentication
        if (-not $TenantId -or -not $ClientId -or -not $ClientSecret) {
          Write-Verbose "Connecting to Microsoft Graph using interactive authentication..."
          Connect-MgGraph -Scopes $Scopes
        }
        # Connect Using Service Principal Authentication
        else {
          Write-Verbose "Connecting to Microsoft Graph using service principal authentication..."
          $SecureSecret = ConvertTo-SecureString -String $ClientSecret -AsPlainText -Force
          $Credential = New-Object System.Management.Automation.PSCredential($ClientId, $SecureSecret)
          Connect-MgGraph -TenantId $TenantId -ClientId $ClientId -ClientSecretCredential $Credential
        }
  
          # Verify connection
          $Context = Get-MgContext
          if ($Context) {
            Write-Verbose "Successfully connected to Microsoft Graph API"
            Write-Verbose "Tenant ID: $($Context.TenantId)"
            Write-Verbose "Account: $($Context.Account)"
            Write-Verbose "Scopes: $($Context.Scopes -join ', ')"
            return $Context
          }
          else {
            throw "Failed to establish connection to Microsoft Graph API"
          }
      }
      catch {
        Write-Error "Error connecting to Microsoft Graph API: $_"
        return $false
      }
    }

    end {

    }
  }

