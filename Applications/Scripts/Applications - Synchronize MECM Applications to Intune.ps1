  # Write-HostFormat
  <#
  .SYNOPSIS
  Writes log output based on the type of the message passed to the function compared to the logging level set during script initialization.
  #>
  Write-Host "    - Write-HostFormat"
  function Write-HostFormat {
  [CmdletBinding(DefaultParameterSetName="Message")]
    param (
      [Parameter(Mandatory=$true, ParameterSetName="Message", HelpMessage="Message for logging.")]
      [string]$Message,
      [Parameter(Mandatory=$true, ParameterSetName="Object", HelpMessage="Message for logging.")]
      [object]$ErrorObject,
      [Parameter(Mandatory=$false, ParameterSetName="", HelpMessage="The log level type of the message.")]
      [ValidateSet("INFO","WARN","ERROR","FATAL","DEBUG")]
      [string]$Type="INFO",
      [Parameter(Mandatory=$false, ParameterSetName="", HelpMessage="Indent level that the message should be displayed at.")]
      [int]$Indent=0,
      [Parameter(Mandatory=$false, ParameterSetName="", HelpMessage="Adds a number of new lines after the last message written.")]
      [int]$NewLines=0,
      [Parameter(Mandatory=$false, ParameterSetName="", HelpMessage="A hashtable set of supported arguments to pass to the Write-Host cmdlet.")]
      [hashtable]$Arguments,
      [Parameter(Mandatory=$false, ParameterSetName="", HelpMessage="Exit code to return to the console and caller.")]
      [int]$ExitCode
    )

    begin {
      # Set Delimiter
      $Delimiter = "  "

      # Associate Log Types to Logging Level
      $LoggingLevels = @{
        Info      = @("INFO","WARN","ERROR","FATAL")
        Debug     = @("INFO","WARN","ERROR","FATAL","DEBUG")
        ErrorOnly = @("ERROR","FATAL")

      }
    }

    process {
      if ($Message) {
        # Construct Strings with Indent
        $Message = ($Delimiter * $Indent) + $Message
  
        # Output
        switch ($Param_Logging) {
          "Info" {
            if ($Type -in $LoggingLevels.Info) {
              Write-Host $Message @Arguments
            }
          }
          "Debug" {
            if ($Type -in $LoggingLevels.Debug) {
              Write-Host $Message
            }
          }
          "ErrorOnly" {
            if ($Type -in $LoggingLevels.ErrorOnly) {
              Write-Host $Message
            }
          }
          Default {}
        }
      }
      elseif ($ErrorObject) {
        # Construct Strings with Indent
        $Message    = ($Delimiter * $Indent) + "Message: " + $ErrorObject.Exception.Message
        $ErrorID    = ($Delimiter * $Indent) + "ErrorId: " + $ErrorObject.Exception.ErrorId
        $Activity   = ($Delimiter * $Indent) + "Activity: " + $ErrorObject.CategoryInfo.Activity
        $Location   = ($Delimiter * $Indent) + "Line/Position: " + $ErrorObject.Exception.Line + " / " + $ErrorObject.Exception.Offset
        $ScriptName = ($Delimiter * $Indent) + "ScriptName: " + $ErrorObject.InvocationInfo.ScriptName
  
        # Output
        Write-Host $Message
        Write-Host $ErrorID
        Write-Host $Activity
        Write-Host $Location
        Write-Host $ScriptName
      }
      else { }
    }

    end {
      # Process NewLines Parameter
      if ($NewLines -gt 0) {
        $Message = "`n" * ($NewLines - 1)
        Write-Host $Message
      }

      # Process Fatal Messages
      if (($Type = "FATAL") -and $ExitCode) {
        # Construct Strings with Indent
        $ExitMessage    = ($Delimiter * $Indent) + "ExitCode: " + $ExitCode

        # Output
        Write-Host $ExitMessage

        # Cleanup
        Stop-Transcript -ErrorAction SilentlyContinue
        Exit $ExitCode
      }
    }
  }

